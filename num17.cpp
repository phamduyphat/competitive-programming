int64_t mul(int64_t a, int64_t b)
{
    int64_t res = 0;
    while (b > 0)
    {
        if (b % 2 == 1)
            res = (res + a);
        a = (a * 2);
        b /= 2;
    }
    return res;
}
int64_t power(int64_t a, int32_t b)
{
    int64_t res = 1;
    a %= b;
    if (a == 0) return 0;
    while(b > 0)
    {
        if (b & 1)
            res = mul(res, a);
        b /= 2;
        a = mul(a, a);
    }
    return res;
}
int64_t Modular_multiplication(int64_t a, int64_t b)
{
    int64_t res = 0;
    a %= MODULO;
    while (b > 0)
    {
        if (b % 2 == 1)
            res = (res + a) % MODULO;
        a = (a * 2) % MODULO;
        b /= 2;
    }
    return res % MODULO;
}
int64_t Modular_exponentiation(int64_t a, int32_t b)
{
    int64_t res = 1;
    a %= b;
    if (a == 0) return 0;
    while(b > 0)
    {
        if (b & 1)
            res = Modular_multiplication(res, a);
        b /= 2;
        a = Modular_multiplication(a, a);
    }
    return res;
}
