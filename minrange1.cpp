//functions declarations:
#define MID (ll + (rr - ll) / 2)
#define Lcon id * 2, ll, MID
#define Rcon id * 2 + 1, MID + 1, rr
int32_t build(int32_t id, int32_t ll, int32_t rr, vector<int32_t> a)
{
    if (ll == rr)
    {
        st[id] = a[ll];
        clgt[rr] = id;
        return st[id];
    }
    st[id] = min(build(Lcon, a), build(Rcon, a));
    return st[id];
}
void update(int32_t id, int32_t ll, int32_t rr, int32_t pos, int32_t val)
{
    id = clgt[pos];
    st[id] = val;
    while(id > 1)
    {
        id /= 2;
        st[id] = min(st[id * 2], st[id * 2 + 1]);
    }
    return;
}
int32_t query(int32_t id, int32_t ll, int32_t rr, int32_t l, int32_t r)
{
    if (ll > r || rr < l) return INT_MAX;
    if (ll >= l && rr <= r) return st[id];
    return min(query(Lcon, l, r), query(Rcon, l, r));
}
